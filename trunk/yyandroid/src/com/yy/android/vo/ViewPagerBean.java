package com.yy.android.vo;

public class ViewPagerBean {
	String name;
	int id;
	String RightCode;

	public String getRightCode() {
		return RightCode;
	}

	public void setRightCode(String rightCode) {
		RightCode = rightCode;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

}
